<?php
/**
 * @package Log
 */

/**
 * Logger interface
 *
 * @package Log
 */
interface Log_Interface
{
    /**
     * Writes an arbitrary message to the log
     *
     * @param mixed $msg Message content
     * @param int   $lvl Verbosity level
     */
    public function write($msg, $lvl=self::LEVEL_INFO);
}
